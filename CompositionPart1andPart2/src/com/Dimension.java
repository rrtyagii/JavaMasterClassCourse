package com;

public class Dimension {

	private int width;
	private int height; 
	private int depth;
	public Dimension(int width, int height, int depth) {
		super();
		this.width = width;
		this.height = height;
		this.depth = depth;
	}
	/**
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}
	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}
	/**
	 * @return the depth
	 */
	public int getDepth() {
		return depth;
	}
	
	
}
