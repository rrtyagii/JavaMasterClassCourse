public class WhiteSPACEIdenting {
	
	public static void main (String args[]) {
		int myvariable=50 ;  // "my variable=50" is an expression
		//the entire line " int my variable = 50 ; " is a statement
		System.out.println(myvariable);
		if(myvariable==50) {
			System.out.println("this is identation ") ; 
		}
		myvariable++ ; 
		System.out.println(myvariable);
		
		myvariable-- ; 
		 
		System.out.println(myvariable);
	 	
		
		
	}
}
